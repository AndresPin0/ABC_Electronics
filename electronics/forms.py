from django import forms


class ChildForm(forms.Form):
    name = forms.CharField(required=False)
    birthdate = forms.DateField(required=False)
    gender = forms.CharField(required=False)
    studies = forms.BooleanField(required=False)
    playsVideoGames = forms.BooleanField(required=False)
    platforms = forms.CharField(required=False)


class LocationForm(forms.Form):
    municipality = forms.CharField(required=False)
    department = forms.CharField(required=False)
    country = forms.CharField(required=False)


class MaritalStatusForm(forms.Form):
    status = forms.CharField(required=False)
    changeDate = forms.DateField(required=False)
    partner = forms.CharField(required=False)


class ClientForm(forms.Form):
    name = forms.CharField(required=False)
    email = forms.EmailField(required=False)
    children = forms.FormField(ChildForm, required=False)
    birthplace = forms.FormField(LocationForm, required=False)
    currentLocation = forms.FormField(LocationForm, required=False)
    interests = forms.CharField(required=False)
    sports = forms.CharField(required=False)
    maritalStatus = forms.FormField(MaritalStatusForm, required=False)
    interestCategories = forms.CharField(required=False)
